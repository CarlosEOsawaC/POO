public class Clientes {
    // id, nome, endereco, telefone, email, cpf
    // crie 4 construtores na classe cliente
    private int id;
    private String nome;
    private String endereco;
    private int telefone;
    private String email;
    private String cpf;

    public Clientes (int id, String nome, String endereco, int telefone, String email, String cpf){
        this.id = id;
        this.nome = nome;
        this.endereco = endereco;
        this.telefone = telefone;
        this.email = email;
        this.cpf = cpf;
    }
}
