public class Condominio {
    private int id;
    private int Napartamento;
    private String Nplaca;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNapartamento() {
        return Napartamento;
    }

    public void setNapartamento(int napartamento) {
        Napartamento = napartamento;
    }

    public String getNplaca() {
        return Nplaca;
    }

    public void setNplaca(String nplaca) {
        Nplaca = nplaca;
    }
}
